#!/bin/bash
set -v
PINNING="numactl -N 0 -m 0"
NTHREADS=16

if [[ -z "${NTHREADS}" ]]; then NTHREADS=32; fi

QPS=2000
MAXREQS=50000
WARMUPREQS=14000

TBENCH_QPS=${QPS} TBENCH_MAXREQS=${MAXREQS} TBENCH_WARMUPREQS=${WARMUPREQS} \
    TBENCH_MINSLEEPNS=10000 ${PINNING} ./mttest_integrated -j${NTHREADS} \
    mycsba masstree
